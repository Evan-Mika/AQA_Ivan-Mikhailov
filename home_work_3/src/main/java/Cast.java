public class Cast {
    public static void main(String[] args) {
        char a = 'a';
        System.out.println(a);

        byte b = 126;
        System.out.println(b);

        short sh = 250;
        System.out.println(sh);

        int i = 600;
        System.out.println(i);


        long l = 9223372036854775L;
        System.out.println(l);

        float f =  10.001234567f;
        System.out.println(f);

        double d = 10.123456789123456789d;
        System.out.println(d);

        int castByteToInt = b;
        System.out.println(castByteToInt);

        int castShortToInt = sh;
        System.out.println(castShortToInt);

        double castFloatToDouble = f;
        System.out.println(castFloatToDouble);

        float castDoubleToFloat = (float) d;
        System.out.println(castDoubleToFloat);





    }
}
